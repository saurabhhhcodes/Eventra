const normalizeThemeTokens = (tokens) => ({
  surfacePage: tokens.surfacePage ?? tokens["--bg-color"],
  surfacePageAlt: tokens.surfacePageAlt ?? tokens["--card-bg-color"],
  surfaceMuted:
    tokens.surfaceMuted ??
    tokens["--bg-secondary"] ??
    tokens["--input-bg"] ??
    tokens["--card-bg-color"],
  surfaceHover: tokens.surfaceHover ?? tokens["--border-color"],
  textPrimary: tokens.textPrimary ?? tokens["--text-color"],
  textSecondary: tokens.textSecondary ?? tokens["--text-color-light"],
  textMuted: tokens.textMuted ?? tokens["--text-color-light"],
  borderDefault: tokens.borderDefault ?? tokens["--border-color"],
  borderInput: tokens.borderInput ?? tokens["--border-color"],
  brandPrimary: tokens.brandPrimary ?? tokens["--primary-color"],
  brandPrimaryStrong: tokens.brandPrimaryStrong ?? tokens["--primary-color"] ?? tokens.brandPrimary,
  brandPrimaryHover:
    tokens.brandPrimaryHover ?? tokens["--primary-hover"] ?? tokens["--primary-color"],
  brandSecondary: tokens.brandSecondary ?? tokens["--secondary-color"],
  brandAccent: tokens.brandAccent ?? tokens["--secondary-color"] ?? tokens.brandSecondary,
  glowBrand: tokens.glowBrand ?? tokens["--glow-color"],
  shadowSubtle: tokens.shadowSubtle ?? "rgba(0, 0, 0, 0.12)",
  dangerSoft: tokens.dangerSoft ?? COLOR_TOKENS.light.dangerSoft,
  dangerBorder: tokens.dangerBorder ?? COLOR_TOKENS.light.dangerBorder,
  dangerText: tokens.dangerText ?? COLOR_TOKENS.light.dangerText,
  successSoft: tokens.successSoft ?? COLOR_TOKENS.light.successSoft,
  successBorder: tokens.successBorder ?? COLOR_TOKENS.light.successBorder,
  successText: tokens.successText ?? COLOR_TOKENS.light.successText,
  warningSoft: tokens.warningSoft ?? COLOR_TOKENS.light.warningSoft,
  warningBorder: tokens.warningBorder ?? COLOR_TOKENS.light.warningBorder,
  warningText: tokens.warningText ?? COLOR_TOKENS.light.warningText,
});

export const createThemeVariables = (tokens) => {
  const theme = normalizeThemeTokens(tokens);

  return {
    ...tokens,
    "--bg-color": theme.surfacePage,
    "--bg-secondary": theme.surfaceMuted,
    "--bg-secondary-color": theme.surfaceMuted,
    "--bg-gradient": `linear-gradient(135deg, ${theme.surfacePage} 0%, ${theme.surfaceMuted} 100%)`,
    "--text-color": theme.textPrimary,
    "--text-color-light": theme.textSecondary,
    "--border-color": theme.borderDefault,
    "--card-bg-color": theme.surfacePageAlt,
    "--card-border": `1px solid ${theme.borderDefault}`,
    "--card-shadow": `0 4px 16px ${theme.shadowSubtle}`,
    "--card-shadow-color": theme.shadowSubtle,
    "--navbar-bg": tokens["--navbar-bg"] ?? theme.surfacePageAlt,
    "--sidebar-bg": tokens["--sidebar-bg"] ?? theme.surfacePageAlt,
    "--input-bg": tokens["--input-bg"] ?? theme.surfacePageAlt,
    "--input-text": tokens["--input-text"] ?? theme.textPrimary,
    "--input-border": theme.borderInput,
    "--input-placeholder": theme.textMuted,
    "--input-focus-border": theme.brandPrimaryStrong,
    "--input-focus-ring": theme.glowBrand,
    "--table-bg": tokens["--table-bg"] ?? theme.surfacePageAlt,
    "--modal-bg": tokens["--modal-bg"] ?? theme.surfacePageAlt,
    "--surface-muted": theme.surfaceMuted,
    "--surface-hover": theme.surfaceHover,
    "--primary-color": theme.brandPrimary,
    "--primary-hover": theme.brandPrimaryHover,
    "--secondary-color": theme.brandSecondary,
    "--color-primary-hover": theme.brandPrimaryHover,
    "--accent-gradient":
      tokens["--accent-gradient"] ??
      `linear-gradient(135deg, ${theme.brandPrimary} 0%, ${theme.brandAccent} 100%)`,
    "--glow-color": theme.glowBrand,
    "--danger-bg": theme.dangerSoft,
    "--danger-border": theme.dangerBorder,
    "--danger-text": theme.dangerText,
    "--success-bg": theme.successSoft,
    "--success-border": theme.successBorder,
    "--success-text": theme.successText,
    "--warning-bg": theme.warningSoft,
    "--warning-border": theme.warningBorder,
    "--warning-text": theme.warningText,
  };
};

export const DEFAULT_THEME_VARIABLES = {
  light: createThemeVariables(COLOR_TOKENS.light),
  dark: createThemeVariables(COLOR_TOKENS.dark),
};
